const ATTRIBUTE_NAMES = ['placeholder', 'title', 'aria-label'];

export function normalizeLanguage(value) {
  return SUPPORTED.has(value) ? value : 'th';
}

export function getLanguage(settings) {
  return normalizeLanguage(settings?.language);
}

export function isEnglish(settingsOrLanguage) {
  const lang = typeof settingsOrLanguage === 'string' ? settingsOrLanguage : getLanguage(settingsOrLanguage);
  return lang === 'en';
}

export function translate(value, language = 'th') {
  const text = String(value ?? '');
  if (normalizeLanguage(language) !== 'en' || !text) return text;
  const leading = text.match(/^\s*/)?.[0] || '';
  const trailing = text.match(/\s*$/)?.[0] || '';
  const core = text.trim();
  if (!core) return text;
  if (EN_EXACT[core]) return `${leading}${EN_EXACT[core]}${trailing}`;
  for (const [pattern, replacement] of EN_PATTERNS) {
    if (pattern.test(core)) return `${leading}${core.replace(pattern, replacement)}${trailing}`;
  }
  return text;
}

export function translateWithPhrases(value, language = 'th') {
  // Translate whole strings only. Partial Thai→English substitution can create mixed,
  // unreadable labels (for example Thai words embedded in English sentences).
  return translate(value, language);
}

export function localizeDom(root, language = 'th') {
  const lang = normalizeLanguage(language);
  if (!root) return;
  if (lang !== 'en') return;
  const doc = root.nodeType === 9 ? root : (root.ownerDocument || document);
  const nodeFilter = doc.defaultView?.NodeFilter || globalThis.NodeFilter;
  if (!nodeFilter) return;
  const walker = doc.createTreeWalker(root, nodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      if (!node.nodeValue?.trim()) return nodeFilter.FILTER_REJECT;
      if (node.parentElement?.closest('[data-i18n-skip],script,style')) return nodeFilter.FILTER_REJECT;
      return nodeFilter.FILTER_ACCEPT;
    }
  });
  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  for (const node of nodes) node.nodeValue = translateWithPhrases(node.nodeValue, lang);
  root.querySelectorAll?.('*').forEach(element => {
    if (element.closest('[data-i18n-skip]')) return;
    for (const attr of ATTRIBUTE_NAMES) {
      if (!element.hasAttribute(attr)) continue;
      element.setAttribute(attr, translateWithPhrases(element.getAttribute(attr), lang));
    }
  });
}

